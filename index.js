function add(a, b){
    return a + b;
}

function subtract(a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function divide(a, b){
    return a / b;
}

function increment(n){
    return n += 1;
}

function decrement(n){
    return n -= 1;
}

function makeInt(num){
    return parseInt(num, 10);
}

function preserveDecimal(float){
    return parseFloat(float);
}